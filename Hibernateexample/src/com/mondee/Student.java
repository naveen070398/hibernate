package com.mondee;

public class Student {
private int sid;
private int smarks;
private String sname;
public int getSid() {
	return sid;
}
public void setSid(int sid) {
	this.sid = sid;
}
public int getSmarks() {
	return smarks;
}
public void setSmarks(int smarks) {
	this.smarks = smarks;
}
public String getSname() {
	return sname;
}
public void setSname(String sname) {
	this.sname = sname;
}

	

}
